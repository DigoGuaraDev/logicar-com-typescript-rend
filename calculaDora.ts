const s1 = 5
const s2 = 10

function soma(s1:number , s2:number):number{
return s1 + s2 
}	
console.log("soma", soma(s1,s2))

function supitrair (s1:number , s2:number):number{
return s1 - s2
}	
 console.log ("supitrair",supitrair(s1,s2))

 function multiplica (s1:number , s2:number):number{
return s1 * s2	
 }
 console.log ("multiplica",multiplica(s1,s2))

 function divição (s1:number , s2:number):number{
return s1 / s2	 
 }	 
 console.log ("divição",divição(s1,s2))
